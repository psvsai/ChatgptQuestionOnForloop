package Chatgpt.Forloop;

import java.util.Scanner;

// Loop through numbers from 1 to 20.
public class ContinueLoop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        for(int i=1;i<=a ;i++){
            System.out.println(i);
        }
    }
    
}
